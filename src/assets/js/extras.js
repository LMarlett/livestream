// Function to toggle navbar into hamburger menu on small screens
function toggleFunction() {
    // function imported and declared in each component.ts file and implemented in the css 
    var x = document.getElementById("myTopnav");
    if (x.className === "topnav") {
        x.className += " responsive";
    } else {
        x.className = "topnav";
    }
}

// Function to toggle panels/dropdowns on FAQ, Logistics, Agenda, and Commitment Examples pages
function toggleFAQ() {
    var acc = document.getElementsByClassName("accordion");
    var i;

    for (i = 0; i < acc.length; i++) {
        acc[i].addEventListener("click", function() {
            this.classList.toggle("active");
            var panel = this.nextElementSibling;
            if (panel.style.maxHeight) {
                panel.style.maxHeight = null;
            } else {
                panel.style.maxHeight = panel.scrollHeight + "px";
            } 
        });
    }
}